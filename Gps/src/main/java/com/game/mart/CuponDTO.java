package com.game.mart;

public class CuponDTO {
	
	private int cup_num;
	private String cup_serial;
	private int cup_price;
	private int cup_check;
	
	
	public int getCup_num() {
		return cup_num;
	}
	public void setCup_num(int cup_num) {
		this.cup_num = cup_num;
	}
	public String getCup_serial() {
		return cup_serial;
	}
	public void setCup_serial(String cup_serial) {
		this.cup_serial = cup_serial;
	}
	public int getCup_price() {
		return cup_price;
	}
	public void setCup_price(int cup_price) {
		this.cup_price = cup_price;
	}
	public int getCup_check() {
		return cup_check;
	}
	public void setCup_check(int cup_check) {
		this.cup_check = cup_check;
	}
	
	
	

}
